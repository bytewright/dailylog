CREATE TABLE app_users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   name VARCHAR(255) NOT NULL,
   role VARCHAR(255),
   created_at_utc TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ,
   updated_at_utc TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ,
   CONSTRAINT pk_app_users PRIMARY KEY (id)
);

CREATE TABLE hemodynamics (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   diastole INT NOT NULL,
   systole INT NOT NULL,
   heart_rate INT,
   created_at_utc TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
   measured_at_utc TIMESTAMP NOT NULL,
   measured_at_iana VARCHAR(255) NOT NULL,
   user_id BIGINT NOT NULL,
   note VARCHAR(255),
   CONSTRAINT pk_hemodynamics PRIMARY KEY (id),
   CONSTRAINT fk_hemodynamics_user_id FOREIGN KEY (`user_id`) REFERENCES `app_users` (`id`) ON DELETE CASCADE
);
